#include<iostream>
#define pie 3.14
using namespace std;
int main()
{
            float r,circum,area;
            cout<<"Enter the radius of circle"<<endl;
            cin>>r;
            circum=2*pie*r;
            area=pie*r*r;
            cout<<"Circumference of circle:"<<circum<<endl;
            cout<<"Area of circle:"<<area<<endl;
 return 0;
}

